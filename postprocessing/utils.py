def max_length_match(text, lang = "hindi"):
    total_length = 0
    max_match = ""
    for sub_text in text:
        if type(sub_text) == tuple:         # Check whether there are multiple matches or not 
            temp = 0
            temp_text = ""
            for single_tuple in sub_text:                       # Check for each match
                if single_tuple != "":
                    if lang == "hindi":
                        temp_text += 1
                    else:
                        temp_text += len(single_tuple)
            if total_length < temp:
                total_length = temp 
                max_match = temp_text
        else:                                               # if single match then we will run this 
            total_length += len(sub_text.strip())
            if len(sub_text)>0:
                max_match += sub_text.strip() + " "

    max_match = max_match.strip()
    if total_length >= 1:                      
        return max_match, total_length + len(max_match.split()) - 1         # this will return the max match sentence and the total length of the match including spaces in between words
    else:
        return max_match, total_length
